from PIL import Image, ImageDraw

def create_monster_sprite(filename, colors, draw_func):
    """创建野怪精灵图"""
    img = Image.new('RGBA', (32, 32), (0, 0, 0, 0))
    draw = ImageDraw.Draw(img)
    draw_func(draw)
    img.save(filename, 'PNG')

